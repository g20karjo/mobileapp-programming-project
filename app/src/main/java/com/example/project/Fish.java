package com.example.project;

public class Fish {
    int size;
    String name;



    String ID;

    public Fish(String name){
        this.name = name;
        this.size = size;
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public int getSize() {
        return size;
    }

    public String getID() {
        return ID;
    }

}
